package com.qixin.model;

public class Zongxian {

	private int num;
	private int addr;

	public Zongxian() {
		super();
	}

	public Zongxian(int num, int addr) {
		super();
		this.num = num;
		this.addr = addr;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getAddr() {
		return addr;
	}

	public void setAddr(int addr) {
		this.addr = addr;
	}

}
